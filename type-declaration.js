/* Primitive Types in TypeScript
    Unlike other Programming languages using Prefix type declaration,
    Typescript uses Postfix type declaration */
var num; //Number Type
var flag; //Boolean Type
var str; //String Type
var foo; //Undefined Data Type
var nothing; //Null Data Type
num = 10;
flag = true;
str = 'Rahul';
//Declaring Array
var numArr; //Number Array
numArr = [1, 2, 3];
numArr.push(4);
numArr.push(5);
numArr.pop();
var strArr; //String Array
strArr = ['rahul', 'panda'];
//Tuples
var tupNumStr; //Tuple with first element as a number, and second as a String
tupNumStr = [1, 'Rahul'];
